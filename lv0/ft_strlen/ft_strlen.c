#include <stdio.h>

int	ft_strlen(char *str)
{
	int	 len;

	len = 0;
	while (*str++)
	{
		len++;
	}
	return (len);
}

int main(int ac, char **av)
{
	printf("%d\n", ft_strlen(av[1]));
}
