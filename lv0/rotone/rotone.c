#include <unistd.h>

void	rotone(char ch)
{
	if ((ch >= 'a' && ch < 'z') || (ch >= 'A' && ch < 'Z'))
	{
		ch++;
		write(1, &ch, 1);
	}
	else if (ch == 'z' || ch == 'Z')
	{
		ch -= 25;
		write(1, &ch, 1);
	}
	else
		write(1, &ch, 1);
}

int main(int ac, char **av)
{
	if (ac == 2)
	{
		int	i;

		i = 0;
		while (av[1][i])
		{
			rotone(av[1][i]);
			i++;
		}
	}
	write(1, "\n", 1);
}
